import UIKit

extension UIColor {
    
    var blue: UIColor {
        return UIColor(named: "blue")!
    }
    
    var green: UIColor {
        return UIColor(named: "green")!
    }
    
    var yellow: UIColor {
        return UIColor(named: "yellow")!
    }
    
    var orange: UIColor {
        return UIColor(named: "orange")!
    }
    
    var red: UIColor {
        return UIColor(named: "red")!
    }
    
    var pink: UIColor {
        return UIColor(named: "pink")!
    }
    
    var purple: UIColor {
        return UIColor(named: "purple")!
    }
    
    
    
}
